package maxb.pro;


public class Place
{
    private int X = 0;
    private int Y = 0;

    public Place(int X, int Y)
    {
        this.X = X;
        this.Y = Y;
    }

    public int getX()
    {
        return X;
    }

    public int getY()
    {
        return Y;
    }

    public void setX(int X)
    {
        this.X = X;
    }

    public void setY(int Y)
    {
        this.Y = Y;
    }
}
